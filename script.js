  document.getElementById("skills-container");

const currentYear =
  document.getElementById("current-year");


// =========================================
// Render Employment Timeline
// =========================================

function renderTimeline() {

  timeline.innerHTML =
    employmentHistory
      .map(
        (job, index) => `
          <button
            class="timeline-item ${index === 0 ? "active" : ""}"
            type="button"
            data-index="${index}"
            role="listitem"
            aria-pressed="${index === 0 ? "true" : "false"}"
          >

            <span class="timeline-role">
              ${job.role}
            </span>

            <span class="timeline-company">
              ${job.company}
            </span>

            <span class="timeline-date">
              ${job.period}
            </span>

          </button>
        `
      )
      .join("");


  const timelineButtons =
    document.querySelectorAll(".timeline-item");


  timelineButtons.forEach((button) => {

    button.addEventListener("click", () => {

      const selectedIndex =
        Number(button.dataset.index);


      timelineButtons.forEach((item) => {

        item.classList.remove("active");

        item.setAttribute(
          "aria-pressed",
          "false"
        );

      });


      button.classList.add("active");

      button.setAttribute(
        "aria-pressed",
        "true"
      );


      renderJobDetails(selectedIndex);

    });

  });

}


// =========================================
// Render Job Details
// =========================================

function renderJobDetails(index) {

  const job =
    employmentHistory[index];


  jobDetails.classList.remove("fade-in");


  /*
    Accessing offsetWidth forces the browser
    to restart the animation when content changes.
  */

  void jobDetails.offsetWidth;


  jobDetails.innerHTML = `

    <p class="job-period">
      ${job.period}
    </p>

    <h3>
      ${job.role}
    </h3>

    <p class="job-meta">
      ${job.company} • ${job.location}
    </p>

    <p class="job-summary">
      ${job.summary}
    </p>

    <ul class="job-achievements">

      ${job.achievements
        .map(
          (achievement) =>
            `<li>${achievement}</li>`
        )
        .join("")}

    </ul>

    <div class="job-environment">

      <strong>
        Technology Environment
      </strong>

      <p>
        ${job.environment}
      </p>

    </div>

  `;


  jobDetails.classList.add("fade-in");

}


// =========================================
// Render Technical Skills
// =========================================

function renderSkills() {

  skillsContainer.innerHTML =
    technicalSkills
      .map(
        (group) => `

          <article class="skill-category">

            <h3>
              ${group.category}
            </h3>

            <div class="skill-tags">

              ${group.skills
                .map(
                  (skill) =>
                    `<span class="skill-tag">${skill}</span>`
                )
                .join("")}

            </div>

          </article>

        `
      )
      .join("");

}


